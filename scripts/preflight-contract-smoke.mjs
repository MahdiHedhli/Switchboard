function makeAppServerResult({
  code,
  state,
  rateLimitStatus,
  rateLimitHost = null,
  openaiAuth = null,
  userAgent = state === 'app_server_unavailable' ? 'unknown' : 'Codex Desktop/0.122.0 (preflight contract smoke)',
  accountType = state === 'app_server_unavailable' ? 'unknown' : 'chatgpt',
  plan = state === 'app_server_unavailable' ? 'unknown' : 'Pro',
  endpoint = rateLimitHost ? `https://${rateLimitHost}/backend-api/wham/usage` : null,
  rateLimitDetails = [],
  rateLimitCoverage = rateLimitDetails.length === 0 ? 'none' : 'typed',
  rateLimitBucketCount = rateLimitDetails.length,
  typedRateLimitBucketCount = rateLimitCoverage === 'typed' ? rateLimitBucketCount : 0,
  verdict = code !== 0 ? 'blocked' : (state === 'available' ? 'ready' : 'attention_required'),
  failureCodes = verdict === 'blocked' ? ['raw_codex_app_server_failed'] : [],
  advisoryCodes = verdict === 'attention_required' ? ['raw_codex_app_server_degraded'] : [],
  message = (() => {
    const baseMessage = rateLimitHost ? `${rateLimitStatus} via ${rateLimitHost}` : rateLimitStatus;
    if (!rateLimitCoverage || rateLimitCoverage === 'typed') {
      return baseMessage;
    }
    if (rateLimitCoverage === 'none' && state === 'available') {
      return baseMessage;
    }
    if (rateLimitBucketCount <= 0) {
      return `${baseMessage} [rate-limits ${rateLimitCoverage}]`;
    }
    return `${baseMessage} [rate-limits ${rateLimitCoverage}, typed ${typedRateLimitBucketCount}/${rateLimitBucketCount}]`;
  })(),
} = {}) {
  return {
    code,
    stderr: '',
    summary: {
      verdict,
      failureCodes,
      advisoryCodes,
      message,
      userAgent,
      accountType,
      plan,
      state,
      rateLimitStatus,
      rateLimitHost,
      openaiAuth,
      endpoint,
      rateLimitCoverage,
      rateLimitBucketCount,
      typedRateLimitBucketCount,
      rateLimitDetails,
    },
  };
}

