function makeOperatorResult({
  code = 0,
  stderr = '',
  profile = 'local-only',
  host = '127.0.0.1',
  localOnly = profile === 'local-only',
  allowRemote = profile === 'remote-trusted',
  operatorTokenConfigured = true,
  operatorTokenSource = operatorTokenConfigured ? 'env' : 'unset',
  operatorTokenFile = undefined,
  manualSubscriptionReplaceEnabled = false,
  protocol = profile === 'remote-trusted' ? 'https' : 'http',
  tlsEnabled = profile === 'remote-trusted',
  tlsCertFile = tlsEnabled ? 'fixture-cert.pem' : undefined,
  tlsKeyFile = tlsEnabled ? 'fixture-key.pem' : undefined,
  tlsCaFile = undefined,
  scopes = {
    taskCreate: 'operator_token',
    taskUpdate: 'operator_token',
    subscriptionRefresh: 'operator_token',
    subscriptionReplace: 'disabled',
  },
  verdict = code === 0 ? 'ready' : 'blocked',
  failureCodes = code === 0 ? [] : ['operator_readiness_failed'],
  advisoryCodes = [],
  message = code === 0
    ? `${profile}; host=${host}`
    : (stderr.match(/AssertionError \[ERR_ASSERTION\]: ([^\n]+)/)?.[1]?.trim() ?? `Operator readiness failed for ${profile}.`),
  problems = code === 0 ? [] : [message],
} = {}) {
  return {
    code,
    stderr,
    summary: {
      profile,
      verdict,
      failureCodes,
      advisoryCodes,
      message,
      problems,
      host,
      localOnly,
      allowRemote,
      operatorTokenConfigured,
      operatorTokenSource,
      operatorTokenFile,
      manualSubscriptionReplaceEnabled,
      protocol,
      tlsEnabled,
      tlsCertFile,
      tlsKeyFile,
      tlsCaFile,
      scopes,
    },
  };
}

