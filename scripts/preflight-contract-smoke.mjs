function assertContractCase({
  codexMode,
  operatorResult,
  providerReadinessResult,
  providerSyncResult,
  appServerResult,
  codexResult,
  expectedFailureCodes,
  expectedAdvisoryCodes,
  expectedReadyChecks,
  expectedAttentionChecks,
  expectedBlockedChecks,
  expectedCheckStates,
  expectedCheckCodes,
  expectedCheckMessages,
  expectedSummary,
  expectedVerdict,
}) {
  const { readyChecks, attentionChecks, blockedChecks } = buildPreflightCheckLists(
    codexMode,
    operatorResult,
    providerReadinessResult,
    providerSyncResult,
    appServerResult,
    codexResult,
  );
  assert.deepEqual(readyChecks, expectedReadyChecks);
  assert.deepEqual(attentionChecks, expectedAttentionChecks);
  assert.deepEqual(blockedChecks, expectedBlockedChecks);
  assert.deepEqual(
    buildPreflightCheckStates(
      codexMode,
      operatorResult,
      providerReadinessResult,
      providerSyncResult,
      appServerResult,
      codexResult,
    ),
    expectedCheckStates,
  );
  assert.deepEqual(
    buildPreflightCheckCodes(
      codexMode,
      operatorResult,
      providerReadinessResult,
      providerSyncResult,
      appServerResult,
      codexResult,
    ),
    expectedCheckCodes,
  );
  assert.deepEqual(
    buildPreflightCheckMessages(
      operatorResult,
      providerReadinessResult,
      providerSyncResult,
      appServerResult,
      codexResult,
    ),
    expectedCheckMessages,
  );

  const { failureCodes, advisoryCodes } = buildPreflightCodes(
    codexMode,
    operatorResult,
    providerReadinessResult,
    providerSyncResult,
    appServerResult,
    codexResult,
  );
  assert.deepEqual(failureCodes, expectedFailureCodes);
  assert.deepEqual(advisoryCodes, expectedAdvisoryCodes);

  const summary = buildPreflightSummary(
    codexMode,
    operatorResult,
    providerReadinessResult,
    providerSyncResult,
    appServerResult,
    codexResult,
    failureCodes,
  );
  assert.equal(summary, expectedSummary);
  assert.equal(preflightVerdict(summary), expectedVerdict);
}

